  function applyKoreanStandardOrthography(text, options) {
    options = options || {};
    var s = String(text || "");
    if (!s) return s;
    var light = !!options.light;
    var maxPass = 3;
    var pass;

    for (pass = 0; pass < maxPass; pass++) {
      var prev = s;
      s = normalizePunctuation(s);
      s = runRules(s, MERGE_SPACING);
      s = runRules(s, light ? SPLIT_SPACING_LIGHT : SPLIT_SPACING);
      s = runRules(s, SPELLING);
      s = s.replace(/\s{2,}/g, " ").trim();
      if (prev === s) break;
    }
    return s;
  }

